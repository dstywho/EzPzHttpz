package org.calgb.test.performance;

public class ProcessResponseBodyException extends Exception {

    public ProcessResponseBodyException()
        {
            super();
        }

    public ProcessResponseBodyException(String message, Throwable cause)
        {
            super(message, cause);
        }

    public ProcessResponseBodyException(String message)
        {
            super(message);
        }

    public ProcessResponseBodyException(Throwable cause)
        {
            super(cause);
        }

}
